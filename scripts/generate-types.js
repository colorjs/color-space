#!/usr/bin/env node
/**
 * Generate types/*.d.ts from the live registry (v3: flat-arg converters, `range`,
 * no min/max/channel/alias). Run: node scripts/generate-types.js
 *
 * The declarations mirror the runtime exactly: `color-space` exports only the
 * default registry + `register` (no named space exports — those never existed at
 * runtime); per-space files type the direct `color-space/<name>.js` imports.
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import space from '../index.js'
import lite from '../lite.js'

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), '..')
const dir = path.join(root, 'types')

const ident = (n) => n.replace(/[^a-zA-Z0-9]/g, '_')
const Iface = (n) => ident(n).replace(/^./, (c) => c.toUpperCase()) + 'Space'
const rangeLit = (r) => r && `[${r.map(([a, b]) => `[${a}, ${b}]`).join(', ')}]`

// space files (spaces/): basename === registered name
const names = fs.readdirSync(path.join(root, 'spaces'))
	.filter((f) => f.endsWith('.js') && space[f.replace(/\.js$/, '')])
	.map((f) => f.replace(/\.js$/, ''))
	.sort()

const banner = '// Generated by scripts/generate-types.js — do not edit by hand\n'

// core interface
fs.writeFileSync(path.join(dir, 'color-space.d.ts'), banner + `
/**
 * A trailing conversion parameter: ycbcr/ypbpr/xvycc take \`kb, kr\` numbers,
 * lms takes a 3×3 matrix (row-major, 9 numbers), uvw takes an illuminant name +
 * observer. On composed paths, params bind to the source space's outgoing edge
 * when it is parametric, else to the target space's incoming edge.
 */
export type ConvertParam = number | string | readonly number[];

/**
 * Convert one space's values to another space, in either calling form:
 * batch — an interleaved array-like of pixels (stride = the space's channel
 * count) → a new \`Float64Array\`: \`rgb.lab([255, 0, 0, 0, 128, 255])\`;
 * scalar — \`rgb.lab(255, 0, 0)\` → \`number[]\`. Trailing params
 * (e.g. ycbcr's kb, kr) follow the channels / the pixel array.
 */
export type Convert = {
	(pixels: ArrayLike<number>, ...params: ConvertParam[]): Float64Array;
	(...channelsAndParams: ConvertParam[]): number[];
	/** the raw scalar conversion behind the batch face */
	scalar?: (...args: ConvertParam[]) => number[];
};

/**
 * A color space: its id, conventional channel \`range\` (required — the hubs
 * derive the batch stride from it), and converters to every other space.
 * The conversion graph is wired at runtime, so targets stay an index signature.
 */
export interface ColorSpace {
	/** space id, e.g. 'rgb' */
	name: string;
	/** conventional channel ranges, e.g. [[0, 255], [0, 255], [0, 255]] */
	range: ReadonlyArray<readonly [number, number]>;
	/** converter to another space, keyed by its name — \`space.rgb.lab(255, 0, 0)\` */
	[to: string]: any;
}
`)

// per-space
for (const n of names) {
	const range = space[n].range
	fs.writeFileSync(path.join(dir, n + '.d.ts'), banner + `import { ColorSpace } from './color-space';

export interface ${Iface(n)} extends ColorSpace {
	name: '${n}';${range ? `\n\trange: ${rangeLit(range)};` : ''}
}

declare const ${ident(n)}: ${Iface(n)};
export default ${ident(n)};
`)
}

// registry barrel: default export + register — exactly the runtime surface
const barrel = (spaceNames, unionName, setDoc) => banner +
	`import { ColorSpace, Convert, ConvertParam } from './color-space';\n\n` +
	`export { ColorSpace, Convert, ConvertParam };\n\n` +
	`/** Every registered space id. */\nexport type ${unionName} =\n\t${spaceNames.map((n) => `'${n}'`).join(' |\n\t')};\n\n` +
	`/** ${setDoc} */\ndeclare const space: { [K in ${unionName}]: ColorSpace } & { [name: string]: ColorSpace };\nexport default space;\n\n` +
	`/** Register a custom space (validated, copied in) and wire conversions to/from every existing space. */\nexport function register(newSpace: ColorSpace): typeof space;\n`

fs.writeFileSync(path.join(dir, 'index.d.ts'),
	barrel(names, 'SpaceName', 'All registered color spaces, keyed by name; every pair (identity included) converts.'))

// lite barrel — the compact hub (wasm-covered spaces, plain JS)
const liteNames = names.filter((n) => lite[n])
fs.writeFileSync(path.join(dir, 'lite.d.ts'),
	barrel(liteNames, 'LiteSpaceName', 'The compact space set (mirrors color-space/wasm coverage), keyed by name.'))

// hub — the wiring machinery behind the registries
fs.writeFileSync(path.join(dir, 'hub.d.ts'), banner + `import { ColorSpace } from './color-space';

/** Build a registry from a list of spaces (shallow-copied in) and wire the conversion graph. */
export function createHub(spaces: ColorSpace[]): { [name: string]: ColorSpace };

/** (Re)wire conversions between every pair of spaces in the registry, identity included. */
export function wire(space: { [name: string]: ColorSpace }): void;

/** Validate a space object for registration (name, range, connectivity); throws on failure. */
export function validate(space: { [name: string]: ColorSpace }, s: ColorSpace): void;
`)

// wasm — the prebuilt kernel over the lite space set
fs.writeFileSync(path.join(dir, 'wasm.d.ts'), banner + `import { LiteSpaceName } from './lite';

export { LiteSpaceName };

/** Spaces the WASM kernel can convert between (the lite set). */
export const spaces: LiteSpaceName[];

/** Zero-copy WASM-backed working buffer: n pixels × 3 interleaved channels. */
export function alloc(n: number): Float64Array;

/** Convert the alloc()'d working buffer between spaces, in place. */
export function convert(from: LiteSpaceName, to: LiteSpaceName, n: number): void;

/** Convert n pixels from src into dst (defaults in place), copying through WASM memory. */
export function convertBatch(from: LiteSpaceName, to: LiteSpaceName, src: ArrayLike<number>, dst?: Float64Array, n?: number): Float64Array;

/**
 * The scalar library's shape over the kernel: \`space.rgb.oklch(r, g, b)\` →
 * \`number[]\`; an alloc()'d buffer converts in place; any other array-like →
 * a new Float64Array. Conversion-call parity with the JS hubs — no \`range\`,
 * metadata, or \`register\` here.
 */
declare const api: {
	[K in LiteSpaceName]: { name: K } & {
		[T in LiteSpaceName]: (a: number | ArrayLike<number>, b?: number, c?: number) => number[] | Float64Array;
	};
};
export default api;
`)

// gl — GLSL/WGSL shader chunks + composer
fs.writeFileSync(path.join(dir, 'gl.d.ts'), banner + `
/** A shader chunk: pure data, three.js-ShaderChunk-style. */
export interface GlChunk {
	name: string;
	/** chunks this one's code calls into */
	deps?: GlChunk[];
	/** channel count when not 3 (kelvin/gray = 1, dsh = 2, cmyk = 4) */
	dim?: number;
	/** neighbour name → [forward fn, inverse fn] GLSL function names */
	edges: { [neighbor: string]: (string | null)[] };
	/** raw GLSL source */
	code: string;
	/** differential-test tolerance override for iterative/table chunks */
	tol?: number;
}

/** Compose a registry from chunk imports; returns { chunks, graph, glsl }. */
export function compose(list?: GlChunk[]): {
	chunks: { [name: string]: GlChunk };
	graph: { [name: string]: { [neighbor: string]: string } };
	glsl: (from?: string | GlChunk | (string | GlChunk)[][], to?: string | GlChunk) => string;
};

/** GLSL source for a conversion between imported chunks (or every direction of one). */
export function glsl(from?: string | GlChunk | (string | GlChunk)[][], to?: string | GlChunk): string;

/** The same conversion, translated to WGSL. */
export function wgsl(from?: string | GlChunk | (string | GlChunk)[][], to?: string | GlChunk): string;

export default glsl;
`)

// whitepoints & gamuts — plain data tables
fs.writeFileSync(path.join(dir, 'whitepoints.d.ts'), banner + `
/** CIE standard illuminant whitepoints, [X, Y=100, Z], keyed by observer (2/10) then illuminant. */
declare const whitepoint: {
	2: { [illuminant: string]: [number, number, number] };
	10: { [illuminant: string]: [number, number, number] };
};
export default whitepoint;
export { whitepoint };
`)
fs.writeFileSync(path.join(dir, 'gamuts.d.ts'), banner + `
/** RGB working-space gamuts: primary chromaticities + white point name. */
declare const gamut: {
	[space: string]: {
		primaries: { r: [number, number]; g: [number, number]; b: [number, number] };
		white: string;
	};
};
export default gamut;
`)

// util
fs.writeFileSync(path.join(dir, 'util.d.ts'), banner +
	`/** 3×3 matrix (row-major, 9 elements) times a 3-vector. */\nexport declare const mat3: (m: number[], x: number, y: number, z: number) => [number, number, number];\n`)

// lut — the .cube exporter (lut.js takes space objects, so it types against ColorSpace)
fs.writeFileSync(path.join(dir, 'lut.d.ts'), banner + `import { ColorSpace } from './color-space';

/** A sampled conversion lattice: 0–1-normalized output triples (3D is red-fastest). */
export interface LutTable {
	from: ColorSpace;
	to: ColorSpace;
	dims: 1 | 3;
	size: number;
	domain: ReadonlyArray<readonly [number, number]>;
	range: ReadonlyArray<readonly [number, number]>;
	data: Float64Array;
	/** Resolve-flavor combined cube: the per-channel 1D tone shaper applied before the 3D lattice */
	shaper?: Float64Array;
	shaperSize?: number;
}

/** Off-lattice deviation vs the direct conversion, as fractions of the target's full scale. */
export interface LutStats {
	max: number;
	median: number;
	n: number;
	/** over samples whose direct result lands inside the target range — the LUT's working set; n is the count actually collected */
	in: { max: number; median: number; share: number; n: number };
}

/** Is the conversion purely per-channel (a transfer curve, no cross-channel term)? */
export function channelwise(from: ColorSpace, to: ColorSpace): boolean;

/** Sample a conversion into a LUT lattice. */
export function table(from: ColorSpace, to: ColorSpace, opts?: { size?: number; dims?: 1 | 3; shaper?: boolean | number }): LutTable;

/** Read one color through the lattice (linear/trilinear), native units on both ends. */
export function apply(tab: LutTable, vals: number[]): number[];

/** Measure the lattice against the direct conversion at random off-lattice points. */
export function verify(tab: LutTable, n?: number): LutStats;

/** Render a conversion as a .cube file (Resolve, Premiere, Final Cut, OBS, ffmpeg; shaper → Resolve/OCIO flavor). */
export function cube(from: ColorSpace, to: ColorSpace, opts?: { size?: number; dims?: 1 | 3; shaper?: boolean | number; title?: string; verify?: number | false }): string;
`)

// icc — RGB working spaces as ICC v2 display profiles
fs.writeFileSync(path.join(dir, 'icc.d.ts'), banner + `import { ColorSpace } from './color-space';

/** D50-adapted colorant matrix + sampled TRC; throws when the space is not matrix×transfer RGB. */
export function colorants(s: ColorSpace, samples?: number): {
	rXYZ: [number, number, number];
	gXYZ: [number, number, number];
	bXYZ: [number, number, number];
	/** 0–1 decode curve samples; null = identity (linear space) */
	trc: Float64Array | null;
	/** Bradford D65→D50 adaptation (row-major 3×3) */
	chad: number[];
};

/** Render a space as an ICC v2 RGB display profile (.icc bytes). */
export function profile(s: ColorSpace, opts?: { description?: string; date?: number[]; samples?: number }): Uint8Array;
`)

// remove stale: wrong-extension .ts and orphans without a .js
const KEEP = ['color-space.d.ts', 'index.d.ts', 'lite.d.ts', 'hub.d.ts', 'wasm.d.ts', 'gl.d.ts', 'whitepoints.d.ts', 'gamuts.d.ts', 'util.d.ts', 'lut.d.ts', 'icc.d.ts']
for (const f of fs.readdirSync(dir)) {
	if (f.endsWith('.ts') && !f.endsWith('.d.ts')) fs.unlinkSync(path.join(dir, f)) // lrgb.ts, xvycc.ts, …
	else if (f.endsWith('.d.ts') && !KEEP.includes(f)) {
		const base = f.replace(/\.d\.ts$/, '')
		if (!names.includes(base)) { fs.unlinkSync(path.join(dir, f)); console.log('removed stale', f) }
	}
}

console.log(`generated ${names.length} space d.ts + core/index/lite/hub/wasm/gl/data/util`)
